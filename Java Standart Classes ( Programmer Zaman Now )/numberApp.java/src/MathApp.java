// Math Class
public class MathApp {
    public static void main(String[] args) {
        var min = Math.min(100, 200);
        System.out.println(min);

        var max = Math.max(100, 200);
        System.out.println(max);

        System.out.println(Math.PI);
        var a = Math.pow(2, 5);
        System.out.println(a);
    }
}
